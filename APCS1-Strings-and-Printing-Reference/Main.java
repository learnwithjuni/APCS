class Main {
  public static void main(String[] args) {
    String forty = "40";
    System.out.println(forty);

    double half = .5;
    System.out.println(half);

    boolean isHot = true;
    System.out.println(isHot);

    System.out.println("hello world");

    // Combining Strings
    System.out.println("Hello world" + forty);

    System.out.print("No new line after this! ");

    // Escape characters
    System.out.print("There will be a new line after this\n");
    System.out.println("See!");
  }
}