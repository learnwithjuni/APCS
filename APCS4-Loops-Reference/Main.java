class Main {
  public static void main(String[] args) {
    
    //For Loops
    for (int i = 0; i < 10; i++) {
		  System.out.println("Hello World!");
	  }

    for(int i = 0; i < 15; i++) {
      System.out.println("i = " + i);
    }

    //While Loops
    int i = 0;
    while (i < 10) {
	    System.out.println("Hello World!");
	    i++;
    }
    
  }
}